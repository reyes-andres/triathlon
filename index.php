<div class="article column1">
    <p class="subti">What is triathlon?</p>
    <p class="columna1">A triathlon is a multiple-stage competition involving the completion of three 
continuous and sequential endurance disciplines. While many variations of the 
sport exist, triathlon, in its most popular form, involves swimming, cycling, 
and running in immediate succession over various distances. Triathletes compete 
for fastest overall course completion time, including timed "transitions" between 
the individual swim, cycle, and run components. The word "triathlon" is of Greek 
origin from τρεις or treis (three) and αθλος or athlos (contest).</p>
