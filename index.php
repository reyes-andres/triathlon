<div class="article column3">
    <p class="subti">Paratriathlon</p>
<p class="columna3">Paratriathlon is a variant of the triathlon for athletes with a physical disability. 
 The sport is governed by the International Triathlon Union (ITU), and will first be held as a Paralympic
 event at the 2016 Summer Paralympics to be held in Rio de Janeiro, Brazil.
The annual ITU Triathlon World Championships includes a paratriathlon sprint distance event with a 750m swim, 20 km cycling using handcycles, bicycles or tandem bicycles with a guide and a 5 km wheelchair or running race. Athletes compete in six categories according to the nature of their physical impairments.</p>
